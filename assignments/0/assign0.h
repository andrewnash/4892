#include <string>


namespace engi4892 {

//! Objects that are available in different colours.
enum class ColouredObject
{
	Car,
	Pen,
};


/**
 * What is the best colour, in your opinion, for an object of a certain kind?
 * For example, you might think that cars should be red and pens should be blue,
 * or (if you're doing much teaching) you might think that pens should be red!
 *
 * @returns  the name of the best colour for an object (e.g., "red")
 */
std::string bestColour(ColouredObject object);

}
